/**
 * Generates dist/embedded-assets.mjs: a manifest that imports every file under
 * dist/client as a Bun "file" import (`with { type: "file" }`). When the bundle
 * entry (bin/canvazz.bundle.mjs) is compiled with `bun build --compile`, these
 * imports get embedded into the single-file binary, so the packaged `canvazz`
 * serves the client without any files on disk.
 *
 * Run after `vite build`, before `bun build --compile`.
 */
import { readdirSync, statSync, writeFileSync } from 'node:fs'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const clientDir = join(root, 'dist', 'client')
const outFile = join(root, 'dist', 'embedded-assets.mjs')

function walk(dir) {
  const out = []
  for (const name of readdirSync(dir)) {
    const full = join(dir, name)
    if (statSync(full).isDirectory()) out.push(...walk(full))
    else out.push(full)
  }
  return out
}

let files
try {
  files = walk(clientDir).sort()
} catch {
  console.error(`[gen-embedded-assets] missing ${relative(root, clientDir)} — run \`bun run build\` first.`)
  process.exit(1)
}

const imports = []
const entries = []
files.forEach((abs, i) => {
  const id = `a${i}`
  // Import path is relative to dist/embedded-assets.mjs (i.e. "./client/...").
  const importPath = `./${relative(join(root, 'dist'), abs).split('\\').join('/')}`
  // URL path the browser requests (relative to dist/client, leading slash).
  const urlPath = `/${relative(clientDir, abs).split('\\').join('/')}`
  imports.push(`import ${id} from ${JSON.stringify(importPath)} with { type: 'file' }`)
  entries.push(`  ${JSON.stringify(urlPath)}: ${id},`)
})

const body = `// AUTO-GENERATED by tools/gen-embedded-assets.mjs — do not edit.
${imports.join('\n')}

/** URL path -> embedded file (Bun.file-readable path inside the compiled binary). */
export const assets = {
${entries.join('\n')}
}
`

writeFileSync(outFile, body)
console.log(`[gen-embedded-assets] embedded ${files.length} client files -> ${relative(root, outFile)}`)
